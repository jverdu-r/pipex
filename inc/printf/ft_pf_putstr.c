#include "printf.h"

int	ft_pf_putstr(char *str)
{
	int		i;
	char	c;

	i = 0;
	if (!str)
	{
		i = 6;
		write (1, "(null)", 6);
		return (i);
	}
	while (str[i])
	{
		c = str[i];
		write (1, &c, 1);
		i++;
	}
	return (i);
}
